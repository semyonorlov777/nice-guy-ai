/** Get welcome config from URL search params */
export function getWelcomeConfig(searchParams: {
  topic?: string;
  tool?: string;
}): WelcomeConfig {
  if (searchParams.topic && TOPIC_WELCOMES[searchParams.topic]) {
    return TOPIC_WELCOMES[searchParams.topic];
  }
  if (searchParams.tool && TOOL_WELCOMES[searchParams.tool]) {
    return TOOL_WELCOMES[searchParams.tool];
  }
  // Default: free chat
  return TOOL_WELCOMES["free-chat"];
}
