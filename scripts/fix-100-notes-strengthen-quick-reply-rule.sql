-- =============================================================
-- Fix 100-notes: усилить блок "Quick replies — ФОРМАТ" в 7 mode-промптах
--
-- Корневая причина R4: модель иногда склеивает «ёлочки» в одну строку
-- через пробел, даже зная про формат. Iter-1 блок описывал формат, но
-- не имел контрпримера "НЕПРАВИЛЬНО" с жирным запретом.
--
-- Что делает:
--   Заменяет блок "### Quick replies — ФОРМАТ (обязательный)..."
--   на усиленную версию с:
--     - явным правилом переноса («перед первой «ёлочкой» — пустая строка»)
--     - запретом склеивания через пробел
--     - контрпримером НЕПРАВИЛЬНО
-- =============================================================

BEGIN;

UPDATE program_modes pm
SET system_prompt = REPLACE(
  pm.system_prompt,
  E'### Quick replies — ФОРМАТ (обязательный)\n\nЕсли хочешь предложить варианты ответа — пиши их в КОНЦЕ сообщения, каждый на отдельной строке, в «ёлочках»:\n\n«Вариант 1 от первого лица»\n«Вариант 2»\n«Мне сложно сформулировать»\n\nЭто ЕДИНСТВЕННЫЙ формат, который платформа рендерит как кнопки. Любой другой формат (список через "—" или "-", нумерация "1. ... 2. ...", блок «Что дальше?» с вопросами, перечисление через "•" как варианты ответа) будет показан пользователю plain-текстом без кнопок.\n\nReplies — от первого лица, голос пользователя. Не вопросы к AI. Не задания. Последний reply в начале диалога — безопасный exit («Мне сложно сформулировать» или аналог).',
  E'### Quick replies — ФОРМАТ (обязательный)\n\nЕсли хочешь предложить варианты ответа — пиши их в КОНЦЕ сообщения, каждый на отдельной строке, в «ёлочках»:\n\n«Вариант 1 от первого лица»\n«Вариант 2»\n«Мне сложно сформулировать»\n\nЭто ЕДИНСТВЕННЫЙ формат, который платформа рендерит как кнопки. Любой другой формат (список через "—" или "-", нумерация "1. ... 2. ...", блок «Что дальше?» с вопросами, перечисление через "•" как варианты ответа) будет показан пользователю plain-текстом без кнопок.\n\nПеред первой «ёлочкой» — пустая строка. Между «ёлочками» — одиночный перенос строки. НИКОГДА не склеивай «ёлочки» через пробел в одну строку.\n\nНЕПРАВИЛЬНО: [вопрос]? «Вариант 1» «Вариант 2»\nНЕПРАВИЛЬНО (все на одной строке): «Вариант 1» «Вариант 2» «Вариант 3»\nПРАВИЛЬНО:\n\n[твой ответ и один вопрос пользователю]\n\n«Вариант 1»\n«Вариант 2»\n«Мне сложно сформулировать»\n\nReplies — от первого лица, голос пользователя. Не вопросы к AI. Не задания. Не дублируй в replies то, что уже спросил в тексте. Последний reply в начале диалога — безопасный exit («Мне сложно сформулировать» или аналог).'
)
FROM mode_templates mt
WHERE pm.mode_template_id = mt.id
  AND mt.key LIKE 'notes_%'
  AND mt.key != 'test_100_notes'
  AND pm.program_id = (SELECT id FROM programs WHERE slug = '100-notes');

-- Variant B для notes_fear_deconstruct (у него был блок с "Не дублируй в
-- replies то, что уже спросил в тексте" — iter-1 variant B).
