-- Добавить "Склеивание «ёлочек» в одну строку через пробел." в ЗАПРЕЩЕНО
-- блок (если он ещё не содержит этот пункт).
UPDATE program_modes pm
SET system_prompt = REPLACE(
  pm.system_prompt,
  E'- «• Вариант» через буллет как reply — это визуально список, не кнопки.\n- Несколько вопросов за ход — только один вопрос за сообщение.',
  E'- «• Вариант» через буллет как reply — это визуально список, не кнопки.\n- Склеивание «ёлочек» в одну строку через пробел.\n- Несколько вопросов за ход — только один вопрос за сообщение.'
)
FROM mode_templates mt
WHERE pm.mode_template_id = mt.id
  AND mt.key LIKE 'notes_%'
  AND mt.key != 'test_100_notes'
  AND pm.program_id = (SELECT id FROM programs WHERE slug = '100-notes');

-- Верификация
SELECT mt.key,
  pm.system_prompt LIKE '%НЕПРАВИЛЬНО (все на одной строке)%' as has_strengthened_rule,
  pm.system_prompt LIKE '%Склеивание «ёлочек» в одну строку через пробел%' as has_forbidden_glue,
  pm.system_prompt LIKE '%НЕ склеивай «ёлочки» через пробел%' as has_rule_text
FROM program_modes pm
JOIN mode_templates mt ON mt.id = pm.mode_template_id
WHERE mt.key LIKE 'notes_%'
  AND mt.key != 'test_100_notes'
  AND pm.program_id = (SELECT id FROM programs WHERE slug = '100-notes')
ORDER BY pm.sort_order;

COMMIT;
