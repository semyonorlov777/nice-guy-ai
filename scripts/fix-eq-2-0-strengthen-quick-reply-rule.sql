-- =============================================================
-- Fix eq-2-0: усилить блок "QUICK REPLIES — «ёлочки»" в 5 mode-промптах
--
-- Корневая причина (см. docs/eq-2-0-fixes-brief.md, проблема #3):
-- В режимах eq_strategies / eq_social_awareness / ... AI выдаёт
-- варианты как plain-text ("Вариант 1", "Вариант 2") вместо «ёлочек» —
-- парсер ChatWindow.tsx:50-73 требует ^[«"](.+?)[»"]$, поэтому кнопок
-- пользователь не видит.
--
-- В прод-БД (на момент написания) лежит УКОРОЧЕННАЯ версия блока
-- (короче, чем в scripts/seed-eq-2-0-modes.sql), например:
--
--   # QUICK REPLIES — «ёлочки»
--   В конце, каждая на отдельной строке, от первого лица. Turns 1-5: ...
--
--   «Вариант 1»
--   «Вариант 2»
--   «Мне сложно сформулировать»
--
--   [НЕ склеивай в строку. НЕ вложенные кавычки. НЕ пунктуация снаружи.]
--   (↑ блок НЕ-правил есть ТОЛЬКО у eq_self_awareness; у 4-х остальных отсутствует)
--
-- Проблема: абстрактные плейсхолдеры «Вариант 1» / «Вариант 2» Gemini
-- трактует как LITERAL output и повторяет дословно (или сливает в plain
-- текст), плюс нет «НИКОГДА не склеивай», нет контрпримеров.
--
-- Что делает: 5 отдельных UPDATE (по одному на режим). REPLACE меняет
-- реальный прод-блок на усиленный с:
--   - конкретными русскими примерами ПОД ТЕМУ каждого режима
--   - правилом «Перед первой «ёлочкой» — пустая строка... НИКОГДА не склеивай»
--   - контрпримерами НЕПРАВИЛЬНО + позитивным ПРАВИЛЬНО
--   - явной фразой «ЕДИНСТВЕННЫЙ формат, который рендерится как кнопки»
--
-- Аналог: scripts/fix-100-notes-strengthen-quick-reply-rule.sql
-- После применения: прод-блок совпадает с scripts/seed-eq-2-0-modes.sql.
-- =============================================================

BEGIN;

-- ─── Режим 1: eq_self_awareness (Что я сейчас чувствую) ───
-- NB: у этого режима в прод есть завершающий блок "НЕ склеивай..." —
-- включаем его в OLD_BLOCK, чтобы полностью вырезать.
UPDATE program_modes pm
SET system_prompt = REPLACE(
  pm.system_prompt,
  E'# QUICK REPLIES — «ёлочки»\nВ конце, каждая на отдельной строке, от первого лица. Turns 1-5: 3-4. 6-12: 2. 13+: 0-1.\n\n«Вариант 1»\n«Вариант 2»\n«Мне сложно сформулировать»\n\nНЕ склеивай в строку. НЕ вложенные кавычки. НЕ пунктуация снаружи.',
  E'# QUICK REPLIES — ФОРМАТ\n\nВ «ёлочках», каждая на отдельной строке, в конце сообщения, от первого лица. Turns 1-5: 3-4 reply. Turns 6-12: 2. Turns 13+: 0-1 exit.\n\nПеред первой «ёлочкой» — пустая строка. Между «ёлочками» — одиночный перенос строки. НИКОГДА не склеивай «ёлочки» через пробел в одну строку.\n\nНЕПРАВИЛЬНО: [вопрос]? «Первый вариант» «Второй вариант»\nНЕПРАВИЛЬНО (все на одной строке): «Вариант 1» «Вариант 2» «Вариант 3»\nПРАВИЛЬНО:\n\n[твой ответ и один вопрос пользователю]\n\n«Это злость, я понял»\n«Обида — но не уверен на кого»\n«Мне сложно назвать»\n\nЭто ЕДИНСТВЕННЫЙ формат, который платформа рендерит как кнопки. Любой другой (списки через «—»/«-», нумерация «1. 2. 3.», буллеты «•», блок «Что дальше?») будет показан plain-текстом без кнопок.\n\nНЕ склеивай в одну строку. НЕ вложенные кавычки. НЕ пунктуация снаружи. НЕ списки через «—» или нумерацию.'
)
FROM mode_templates mt, programs p
WHERE pm.mode_template_id = mt.id AND pm.program_id = p.id
  AND p.slug = 'eq-2-0' AND mt.key = 'eq_self_awareness';


