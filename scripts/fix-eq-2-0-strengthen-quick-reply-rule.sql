-- =============================================================
-- Верификация — каждая строка должна быть TRUE по всем колонкам
-- =============================================================
SELECT mt.key,
  pm.system_prompt LIKE '%НЕПРАВИЛЬНО (все на одной строке)%' AS has_strengthened_rule,
  pm.system_prompt LIKE '%НИКОГДА не склеивай%'              AS has_niggda_rule,
  pm.system_prompt NOT LIKE '%# QUICK REPLIES — «ёлочки»%'    AS old_header_removed,
  (pm.system_prompt LIKE '%«Это злость, я понял»%'
    OR pm.system_prompt LIKE '%«Попробую дыхание в следующий раз»%'
    OR pm.system_prompt LIKE '%«Начну с тона голоса»%'
    OR pm.system_prompt LIKE '%«Готов начать симуляцию»%'
    OR pm.system_prompt LIKE '%«Беру эту практику на неделю»%'
  ) AS has_concrete_example
FROM program_modes pm
JOIN mode_templates mt ON mt.id = pm.mode_template_id
JOIN programs p ON p.id = pm.program_id
WHERE p.slug = 'eq-2-0'
  AND mt.key IN ('eq_self_awareness','eq_self_management','eq_social_awareness','eq_relationships','eq_strategies')
ORDER BY pm.sort_order;

