### ta_ego_imbalance
🟢 low: Твой Взрослый — на руле. Чувствуешь, оцениваешь, выбираешь.
🟡 medium: Бывают «захваты»: Родитель критикует или Адаптивный Ребёнок угождает вместо того, чтобы Взрослый принял решение.
🔴 high: Взрослый часто отключён. Реагируешь на автомате — критикой Родителя или испугом Ребёнка.

### ta_script_power
🟢 low: Ты живёшь свою жизнь, а не повторяешь чужой план. Решения — здесь и сейчас.
🟡 medium: Сценарий проявляется в отдельных областях. В других — ты свободен.
🔴 high: Жизнь идёт по «программе» из детства. Узнаваемые паттерны: «всегда», «никогда», «снова и снова».

### ta_stroke_deficit
🟢 low: У тебя есть источники признания, ты умеешь принимать и давать.
🟡 medium: Признания меньше, чем хотелось бы. Иногда соглашаешься на «не очень», лишь бы не оставаться без внимания.
🔴 high: Сильный «голод по поглаживаниям». Лучше скандал, чем тишина. Можешь провоцировать конфликты ради внимания.

### ta_drama_roles
🟢 low: Ты замечаешь манипуляции и не входишь в роли.
🟡 medium: В отдельных отношениях бываешь то Спасателем, то Жертвой. Вне их — нормально.
🔴 high: Постоянная циркуляция по треугольнику. Спасал — стал Жертвой. Жаловался — стал Преследователем.
</interpretations>

