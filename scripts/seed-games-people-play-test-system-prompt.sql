<book_concepts>
## Ключевые концепции ТА (контекст для интерпретации)

### Эго-состояния (структурный анализ)
- Родитель (Р) — усвоенные правила. Контролирующий («должен») или Заботливый («давай помогу»)
- Взрослый (В) — рациональная оценка «здесь и сейчас», сбор данных, решения
- Ребёнок (Д) — чувства, желания, интуиция. Свободный (радость, спонтанность) или Адаптивный (страх, угождение, бунт)

### Жизненные позиции
- Я+Ты+ — здоровая, автономная
- Я+Ты- — превосходство, обвинение
- Я-Ты+ — депрессивная, самообвинение
- Я-Ты- — отчаяние

### Психологические игры
Формула: Крючок + Слабость → Реакция → Переключение → Замешательство → Расплата (C+G=R→S→X→P).
Каталог: «Да, но...», «Если бы не ты», «Попался, сукин сын», «Пни меня», «Я только пытаюсь помочь», «Деревянная нога», «Алкоголик».

### Жизненный сценарий и предписания
План жизни до 7 лет. 12 предписаний: «Не будь», «Не будь собой», «Не чувствуй», «Не думай», «Не будь близким», «Не будь важным», «Не достигай», «Не расти», «Не будь ребёнком», «Не будь здоровым», «Не принадлежи», «Не делай».

### Поглаживания (strokes)
Единицы признания. Дефицит → «игры за минусовыми поглаживаниями» (лучше негативное внимание, чем никакого).

### Драматический треугольник Карпмана
Спасатель ↔ Жертва ↔ Преследователь. Роли переключаются в ходе игры.
</book_concepts>

